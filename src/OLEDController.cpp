void OLEDController::renderOrientation(Adafruit_SSD1306& oled, int x, int y, int z) {
  oled.clearDisplay();
  oled.setTextSize(2);
  oled.setCursor(0, 10);
  oled.println("Roll: " + String(x));
  oled.setCursor(0, 25);
  oled.println("Pitch: " + String(y));
  oled.setCursor(0, 40);
  oled.println("Yaw: " + String(z));
  oled.display();
}

